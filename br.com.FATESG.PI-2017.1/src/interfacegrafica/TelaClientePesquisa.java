                            jFormattedTextFieldCPF.setText(objPessoaPesq.getCpf());
                            data = formato.parse(alPessoaFisica.get(i).getDataDeNasc());
                            c.setTime(data);
                            jFormattedTextFieldDataNascimento.setText(df.format(c.getTime()));
